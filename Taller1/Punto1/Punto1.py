#1 Realize un programa donde se soliciten dos numeros al usuario, despues se de como resultado el residuo y el cociente de la division entre <n1> y <2>.
n1 = int(input("ingrese un numero: "));
n2 = int(input("ingrese un segundo numero: "));
div = n1/n2;
residuo = n1%n2;
print("el resultado de la divicion de ",n1," entre ",n2," es: ",div," con un residuo de: ",residuo);
